ORG 4000H; Esta subrutina comprueba que el carácter sea un dígito
COMPROBAR: CMP BYTE PTR [BX], 30H
JC NO_ES
CMP BYTE PTR [BX], 39H
JZ SI_ES
JNC NO_ES
SI_ES: MOV CL, 0FFH
JMP TERMINAR
NO_ES: MOV CL, 00H
TERMINAR: RET


;Constantes
PIO EQU 30H

ORG 1000H
LETRA DB "A"

ORG 3000H
CONF_PIO: MOV AL, 01H
OUT PIO+2, AL
MOV AL, 00H
OUT PIO+3, AL
RET

ORG 3100H
CONSULTA: IN AL, PIO
AND AL, 01H
JNZ CONSULTA
RET

ORG 3200H
STROBE: IN AL, PIO
AND AL, 0FDH
OUT PIO, AL
IN AL, PIO
OR AL, 02H
OUT PIO, AL
RET

ORG 2000H
CALL CONF_PIO
MOV BX, OFFSET LETRA
CALL CONSULTA
MOV AL, [BX]
OUT PIO+1, AL
CALL STROBE
INT 0
END
